let choice = 10;
switch (choice) {
  case 1:
    console.log("good morning");
    break;
  case 2:
    console.log("good afternoon");
    break;
  case 3:
    console.log("good evening");
    break;
  case 4:
    console.log("good night");
    break;
  default:
    console.log("wrong choice");
    break;
}
